import fs from 'node:fs/promises';
import path from 'node:path';
import crypto from 'node:crypto';

const root = process.cwd();
const codexImagesRoot = path.join(process.env.USERPROFILE, '.codex', 'generated_images');
const episodeIds = ['ep962-rm-notification-focus-drain', 'ep963-zm-delivery-sms-phishing-trap'];

const sha256File = async (filePath) => crypto.createHash('sha256').update(await fs.readFile(filePath)).digest('hex');
const sha256Text = (text) => crypto.createHash('sha256').update(text, 'utf8').digest('hex');
const slash = (value) => value.replaceAll('\\', '/');

const walkFiles = async (dir) => {
  const out = [];
  const entries = await fs.readdir(dir, {withFileTypes: true});
  for (const entry of entries) {
    const full = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      out.push(...(await walkFiles(full)));
    } else if (entry.isFile()) {
      out.push(full);
    }
  }
  return out;
};

const generatedFiles = await walkFiles(codexImagesRoot);
const generatedByHash = new Map();
for (const file of generatedFiles) {
  const hash = await sha256File(file);
  generatedByHash.set(hash, file);
}

for (const episodeId of episodeIds) {
  const episodeDir = path.join(root, 'script', episodeId);
  const prompts = JSON.parse(await fs.readFile(path.join(episodeDir, 'image_prompts.json'), 'utf8')).prompts;
  const meta = JSON.parse(await fs.readFile(path.join(episodeDir, 'meta.json'), 'utf8'));
  const imageEntries = [];
  const manifestImages = [];

  for (let i = 1; i <= 10; i += 1) {
    const sceneId = `s${String(i).padStart(2, '0')}`;
    const relFile = `assets/${sceneId}_main.png`;
    const filePath = path.join(episodeDir, relFile);
    const assetHash = await sha256File(filePath);
    const sourcePath = generatedByHash.get(assetHash);
    if (!sourcePath) {
      throw new Error(`${episodeId}/${relFile}: source generated image not found by hash`);
    }
    const prompt = prompts[`${sceneId}.main`].imagegen_prompt;
    const relativeSource = slash(path.relative(codexImagesRoot, sourcePath));
    const sourceUrl = `codex://generated_images/${relativeSource}`;
    const scene = sceneId;
    imageEntries.push({
      file: relFile,
      type: 'image',
      source_type: 'imagegen',
      generation_tool: 'codex-imagegen',
      source_url: sourceUrl,
      rights_confirmed: true,
      license: 'AI generated by codex-imagegen for this episode',
      credit_required: false,
      scene_id: scene,
      slot: 'main',
      purpose: 'script_final直投げ型の挿入画像',
      adoption_reason: 'sceneの要点に一致',
      imagegen_prompt: prompt,
    });
    manifestImages.push({
      scene_id: scene,
      slot: 'main',
      file: relFile,
      source_url: sourceUrl,
      original_file: path.basename(sourcePath),
      prompt_sha256: sha256Text(prompt),
    });
  }

  const existingNonImages = Array.isArray(meta.assets)
    ? meta.assets.filter((asset) => !String(asset?.file ?? '').startsWith('assets/'))
    : [];
  meta.image_generation = {
    status: 'PASS',
    generator: 'codex-imagegen',
    generated_count: imageEntries.length,
    placeholder: false,
  };
  meta.assets = [...imageEntries, ...existingNonImages];
  await fs.writeFile(path.join(episodeDir, 'meta.json'), `${JSON.stringify(meta, null, 2)}\n`, 'utf8');
  await fs.writeFile(
    path.join(episodeDir, 'imagegen_manifest.json'),
    `${JSON.stringify({version: 1, images: manifestImages}, null, 2)}\n`,
    'utf8',
  );
  console.log(`${episodeId}: ${imageEntries.length} image ledger entries`);
}
