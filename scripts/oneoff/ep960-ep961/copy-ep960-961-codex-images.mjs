import fs from 'node:fs/promises';
import path from 'node:path';
import os from 'node:os';

const rootDir = process.cwd();
const batchDir = 'C:/temp/codex-img-batch-ep960-961';
const manifest = JSON.parse(await fs.readFile(path.join(batchDir, 'manifest.json'), 'utf8'));

const sessionRe = /session id:\s*([0-9a-f-]+)/i;
const copied = [];
const missing = [];
const sourceUpdates = new Map();

for (const job of manifest) {
  let log;
  try {
    log = await fs.readFile(job.logPath, 'utf8');
  } catch (error) {
    missing.push({label: job.label, reason: `missing log: ${error.message}`});
    continue;
  }
  const match = log.match(sessionRe);
  if (!match) {
    missing.push({label: job.label, reason: 'missing session id'});
    continue;
  }
  const sessionId = match[1];
  const imageDir = path.join(os.homedir(), '.codex', 'generated_images', sessionId);
  let files;
  try {
    files = (await fs.readdir(imageDir)).filter((file) => file.toLowerCase().endsWith('.png'));
  } catch (error) {
    missing.push({label: job.label, sessionId, reason: `missing image dir: ${error.message}`});
    continue;
  }
  if (files.length === 0) {
    missing.push({label: job.label, sessionId, reason: 'no png files'});
    continue;
  }
  files.sort();
  const src = path.join(imageDir, files[0]);
  const dest = path.join(rootDir, job.dest);
  await fs.mkdir(path.dirname(dest), {recursive: true});
  await fs.copyFile(src, dest);
  copied.push({label: job.label, sessionId, src, dest: job.dest});
  sourceUpdates.set(job.dest.replaceAll('\\', '/').replace(/^script\/[^/]+\//, ''), `codex://generated_images/${sessionId}/${files[0]}`);
}

for (const episodeId of new Set(manifest.map((job) => job.episodeId))) {
  const metaPath = path.join(rootDir, 'script', episodeId, 'meta.json');
  const meta = JSON.parse(await fs.readFile(metaPath, 'utf8'));
  let generatedCount = 0;
  for (const asset of meta.assets ?? []) {
    const sourceUrl = sourceUpdates.get(asset.file);
    if (sourceUrl) {
      asset.source_url = sourceUrl;
      asset.license = 'AI generated by gpt-image-1 via Codex CLI for this episode; subject to image generation service terms';
      generatedCount += 1;
    }
  }
  meta.image_generation = {
    ...(meta.image_generation ?? {}),
    status: generatedCount === (meta.assets?.filter((asset) => asset.type === 'image').length ?? 0) ? 'PASS' : 'PARTIAL',
    generator: 'codex-imagegen',
    generated_count: generatedCount,
  };
  await fs.writeFile(metaPath, JSON.stringify(meta, null, 2) + '\n', 'utf8');
}

console.log(JSON.stringify({copied: copied.length, missing, copiedLabels: copied.map((item) => item.label)}, null, 2));
if (missing.length > 0) process.exitCode = 1;
